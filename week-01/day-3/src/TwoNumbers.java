public class TwoNumbers {
    public static void main(String[] args) {
        System.out.println("22 + 13 = : " + (22+13));
        System.out.println("22 - 13 = : " + (22-13));
        System.out.println("22 * 13 = : " + (22*13));
        System.out.println("22 / 13 = : " + (22./13));
        System.out.println("Integer of 22 / 13 = : " + (22/13));
        System.out.println("Remainder of 22 / 13 = : " + (22%13));
    }
}
